///////////////////////////////////////////////////////////////////////////////////////
//defines a javascript object describing a fret
///////////////////////////////////////////////////////////////////////////////////////
function fret(){
this.note = "";
this.x = 0;
this.y = 0;
this.marked = 0;
this.color = "";
///////////////////////////////////////////////////////////////////////////////////////
//sets the X and Y coordinate for the fret
///////////////////////////////////////////////////////////////////////////////////////
this.setPos = function(x1,y1){
	this.x = x1;
	this.y = y1;
};
};